import streamlit as st

# ページ設定
st.set_page_config(
    page_title="相続ナレッジシステム",
    page_icon="📚",
    layout="wide"
)

# メイン画面
st.title("📚 相続ナレッジシステム")
st.caption("Gemini 2.5 Flash + RAG")

st.markdown("""
## ようこそ！

このシステムでは、相続に関する記事を検索したり、AIチャットボットと対話したりできます。

### 📱 機能

""")

col1, col2 = st.columns(2)

with col1:
    st.markdown("""
    ### 🔍 検索
    
    記事を検索して、相続に関する情報を見つけます。
    
    **機能:**
    - タイトル検索
    - AI回答生成
    - 3種類のEmbedding選択
    - 検索件数調整
    
    左側のサイドバーから「🔍 検索」をクリックしてください。
    """)

with col2:
    st.markdown("""
    ### 💬 コラムチャット
    
    記事を選んで、AIとチャットしながら質問できます。
    
    **機能:**
    - ランダムに10記事を表示
    - 記事選択後、チャットで質問
    - チャット履歴の保持
    - 記事内容に基づいた回答
    
    左側のサイドバーから「💬 コラムチャット」をクリックしてください。
    """)

st.divider()

st.markdown("""
### 🚀 使い方

1. 左側のサイドバーから使いたい機能を選択
2. **検索**: キーワードを入力して記事を検索
3. **コラムチャット**: 記事を選んでAIとチャット

### 💡 ヒント

- 検索では複数のEmbedding方式を試すことができます
- チャットでは記事の内容に基づいた詳しい説明が得られます
- チャット履歴は記事ごとに保持されます
""")

